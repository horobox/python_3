my_favourite_places = ['Tokyo', 'Switzerland', 'California', 'Mexico']
print(my_favourite_places)

print(sorted(my_favourite_places))
print(my_favourite_places)

print(sorted(my_favourite_places, reverse=True)) #reversing order using sorted
print(my_favourite_places)

my_favourite_places.reverse()
print(my_favourite_places)

my_favourite_places.reverse()
print(my_favourite_places)

my_favourite_places.sort()
print(my_favourite_places)

my_favourite_places.sort(reverse=True)
print(my_favourite_places)